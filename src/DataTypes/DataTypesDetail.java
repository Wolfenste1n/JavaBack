package DataTypes;

public class DataTypesDetail {
    int myNum = 5; // Integer
    float myFloatNum = 3.14f; // Floating point number
    char myLetter = 'a'; // Character
    boolean myBool = true;
    String myString = "Hello";

    // Data types: Primitive || Non-primitive
    // Primitive: byte, short, int, float, double, boolean, char
    // Non-primitive: String, Arrays, Classes

    // Primitive:
    // byte: -128 -> 127 (whole num)
    // short: -32.768 -> 32.767
    // int: -2.147.483.648 -> 2.147.483.647
    // long: -9.223.372.036.854.775.808 -> 9.223.372.036.854.775.807
    // float: fractional num (6 to 7 decimal digits(после запятой потом округление)) (.f)
    // double: fractional num (15 to 16 decimal digits) (.d or none)
    // boolean: true // false

    // We cannot change int myNum = 5; to myNum = "Hello"; only myNum = 10;

}
